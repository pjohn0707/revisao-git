// exercicios de operações de cálculos

const num1 = 236;
const num2 = 95;

//soma

let soma = num1 + num2;
console.log("Resultado: " + soma);

//subtração

let subtracao = num1 - num2;
console.log("Resultado: " + subtracao);

// multiplicação

let multiplicacao = num1 * num2;
console.log("Resultado: " + multiplicacao);

// divisão

let divisao = num1 / num2;
console.log("Resultado: " + divisao);

// números pares ou ímpares


if(num1 % 2 === 0) {
    console.log("Num1 é par");
} else{
    console.log("Num1 é impar");
}

if(num2 % 2 === 0) {
    console.log("Num2 é par");
} else{
    console.log("Num2 é impar");
}
